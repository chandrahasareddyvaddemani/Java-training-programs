
public class Unaryoperators {
	public static void main(String[]args) {
		//unary operates
		int a=10;
		System.out.println( a++);//increment operator  post-increment by 1
		System.out.println(a);// value 11
		//System.out.println( ++a); // pre-increment
		System.out.println( a--); // post-decrement operater
		System.out.println( --a);//pre-decrement
	}

}